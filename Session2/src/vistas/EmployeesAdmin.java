    public void mostrar(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id");
        modelo.addColumn("nombre");
        modelo.addColumn("apellido");
        modelo.addColumn("telefono");
        modelo.addColumn("Es administrador");
        modelo.addColumn("nombreUsuario");
        modelo.addColumn("contraseÃ±a");
        jTable_employee.setModel(modelo);
        String [] datos = new String[7];
        try {
            for(int i=0; i < lista.size(); i++){
                employeeVO = lista.get(i);
                datos[0] = employeeVO.getId();
                datos[1] = employeeVO.getFirstName();
                datos[2] = employeeVO.getLastName();
                datos[3] = employeeVO.getPhone();
                datos[4] = employeeVO.getIsAdmin();
                datos[5] = employeeVO.getUserName();
                datos[6] = employeeVO.getPassword();
                modelo.addRow(datos);
            }
        } catch (Exception e) {
        }
        
    }

