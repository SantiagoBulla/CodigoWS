    private void jButton_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_okActionPerformed
        userName = jTextField_username.getText();
        password = jPasswordField_password.getText();
        //instancia DAO y VO
        EmployeeVO employeeVO = new EmployeeVO(id, firstName, lastName, phone, isAdmin, userName, password);
        EmployeeDAO employeeDAO = new EmployeeDAO(employeeVO);
        if (!userName.isEmpty() && !password.isEmpty()) {
            if (employeeDAO.inicioSesion(userName, password) && employeeDAO.validacionCredencial(userName, password) ) {
                JOptionPane.showMessageDialog(null,"inicio correcto y es admin");
            }else if(employeeDAO.inicioSesion(userName, password) && !employeeDAO.validacionCredencial(userName, password)){
                JOptionPane.showMessageDialog(null,"inicio correcto contable");

            }else{
                JOptionPane.showMessageDialog(null,"El usuario y/ o contrase√±a no existen en el sistema");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese los datos en los campos de ingreso");
        }
        
    }//GEN-LAST:event_jButton_okActionPerformed

