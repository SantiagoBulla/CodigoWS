    private void jTable_tadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_tadosMouseClicked
        jCheckBox_isadmin.setSelected(false);
        int empleadoSeleccionado = jTable_tados.rowAtPoint(evt.getPoint());
        jLabel_id.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 0)));
        jTextField_name.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 1)));
        jTextField_lastname.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 2)));
        jTextField_phone.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 3)));
        /*String valorIsAdmin = String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 4));
        int valorIntIsAdmin = Integer.valueOf(valorIsAdmin);
        if (valorIntIsAdmin == 1) {
            jCheckBox_isadmin.setSelected(true);
        }*/
        if (jTable_tados.getValueAt(empleadoSeleccionado,4) == null) {
            jCheckBox_isadmin.setSelected(false);
            jCheckBox_isadmin.setText("is Admin: not selected");
        }else{
            String valorIsAdmin = String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 4));
            int valorIntIsAdmin = Integer.valueOf(valorIsAdmin);
            if (valorIntIsAdmin == 1) {
                jCheckBox_isadmin.setSelected(true);
                jCheckBox_isadmin.setText("is Admin: selected");
            }else{
                jCheckBox_isadmin.setSelected(false);
                jCheckBox_isadmin.setText("is Admin: not selected");
            }
        }
        //jCheckBox_isadmin.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado,4)));
        jTextField_username.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 5)));
        jTextField_password.setText(String.valueOf(jTable_tados.getValueAt(empleadoSeleccionado, 6)));

    }//GEN-LAST:event_jTable_tadosMouseClicked

    

