    private void jButton_modificarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_modificarEmpleadoActionPerformed
        this.id = String.valueOf(jLabel_id.getText());
        this.firstName =String.valueOf(jTextField_name.getText()) ;
        this.lastName = String.valueOf(jTextField_lastname.getText()) ;
        this.phone = String.valueOf(jTextField_phone.getText()) ;
        if (jCheckBox_isadmin.isSelected()) {
            isAdmin = "1";
        } else {
            isAdmin = "0";
        }
        //this.isAdmin = String.valueOf(jCheckBox_isadmin) ;
        this.userName = String.valueOf(jTextField_username.getText()) ;
        this.password = String.valueOf(jTextField_password.getText()) ;
        EmpleadoVO empleado_VO = new EmpleadoVO(id, firstName, lastName, phone, isAdmin, userName, password);
        EmpleadoDAO empleado_DAO = new EmpleadoDAO(empleado_VO);
        empleado_DAO.modificarEmpleado();
        GestionUsuarios pantalla = new GestionUsuarios();
        pantalla.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton_modificarEmpleadoActionPerformed

